    public function getTasksForEmployee($employee_id) {
        $tasks = Task::where('employee', $employee_id)->get();
        
        $formattedTasks = $tasks->map(function($task) {
            return [
                'title' => $task->pickup_Customer, // or any other field you want to display
                'start' => $task->pickup_date . 'T' . $task->pickup_Time, // Combine date and time
                // Add any other fields needed by FullCalendar
            ];
        });
    
        return response()->json($formattedTasks);
    }

